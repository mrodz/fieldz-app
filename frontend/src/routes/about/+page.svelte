	<h1 class="h1">What Do We Do?</h1>

	<p>I want to create an app to <b>simplify the process of reserving field space</b> for youth sports.</p>

	<p>In my community, this is mostly done via one massive spreadsheet that is updated by hand. This creates a plethora of logistical issues and is not scalable&hellip; so I came up with this solution to help
	youth sports managers!</p>

	<p>This app will help district managers of youth sports organize and deal out field space for coaches. Coaches will be able to request field space at a time slot, and administrators can heed these requests or deal out space on their own.</p>

	<p>Check back later for more news, because this app is in alpha-prerelease.</p>
